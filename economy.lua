    if input.UserInputType == Enum.UserInputType.MouseButton1 then
        wasDragging = false
    end
end)

toggleBtn.InputEnded:Connect(function(input)
    if input.UserInputType == Enum.UserInputType.MouseButton1 then
        if not wasDragging then
            menuOpen = not menuOpen
            menuFrame.Visible = menuOpen
        end
    end
end)

