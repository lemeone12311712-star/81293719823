local function getColor(profile)
    if profile == "safe" then
        return Color3.fromRGB(0, 255, 0)
    elseif profile == "normal" then
        return Color3.fromRGB(0, 150, 255)
    elseif profile == "risky" then
        return Color3.fromRGB(255, 220, 0)
    elseif profile == "trap" then
        return Color3.fromRGB(255, 0, 0)
    elseif profile == "jackpot" then
        return nil -- радуга отдельно
    end
    return Color3.fromRGB(255, 255, 255)
end

local function applyRainbow(label)
    task.spawn(function()
        local hue = 0
        while label and label.Parent do
            hue = (hue + 0.01) % 1
            label.TextColor3 = Color3.fromHSV(hue, 1, 1)
            task.wait(0.05)
        end
    end)
end

