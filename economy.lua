    if obj:IsA("TextLabel") then
        local profile = EconomyProfiles[obj.Text]
        if profile then
            if profile == "jackpot" then
                applyRainbow(obj)
            else
                obj.TextColor3 = getColor(profile)
            end
        end
    end
end)

